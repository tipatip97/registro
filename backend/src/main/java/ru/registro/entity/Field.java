package ru.registro.entity;

import javax.persistence.*;

@Entity
@Table
public class Field {
    @Id
    @Column
    @GeneratedValue
    private Long id;

    @ManyToOne
    private User user;

    @Column
    private String form;

    @Column
    private String name;

    @Column
    private String value;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public String getForm() {
        return form;
    }

    public void setForm(String form) {
        this.form = form;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
